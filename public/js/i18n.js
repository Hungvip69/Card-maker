const STORE_KEY = 'cardmaker.lang';

export function detectLang() {
  const saved = localStorage.getItem(STORE_KEY);
  if (saved && I18N[saved]) return saved;
  const nav = (navigator.language || 'en').slice(0, 2).toLowerCase();
  return I18N[nav] ? nav : 'en';
}

let current = detectLang();
export function getLang() { return current; }
export function setLang(code) {
  if (!I18N[code]) return;
  current = code;
  localStorage.setItem(STORE_KEY, code);
  document.documentElement.lang = code;
  applyI18n();
  window.dispatchEvent(new CustomEvent('langchange', { detail: code }));
}

/** Lấy chuỗi theo key cho ngôn ngữ hiện tại (fallback English rồi tới key). */
export function t(key, vars) {
  const dict = I18N[current] || I18N.en;
  let s = dict[key] ?? I18N.en[key] ?? key;
  if (vars) for (const [k, v] of Object.entries(vars)) s = s.replace('{' + k + '}', v);
  return s;
}

/** Áp dụng bản dịch cho mọi phần tử có data-i18n / data-i18n-ph / data-i18n-html. */
export function applyI18n(root = document) {
  root.querySelectorAll('[data-i18n]').forEach((el) => { el.textContent = t(el.dataset.i18n); });
  root.querySelectorAll('[data-i18n-html]').forEach((el) => { el.innerHTML = t(el.dataset.i18nHtml); });
  root.querySelectorAll('[data-i18n-ph]').forEach((el) => { el.setAttribute('placeholder', t(el.dataset.i18nPh)); });
  root.querySelectorAll('[data-i18n-title]').forEach((el) => { el.setAttribute('title', t(el.dataset.i18nTitle)); });
}

/** Dựng bộ chọn ngôn ngữ vào một phần tử <select> hoặc menu. */
export function buildLangSwitcher(selectEl) {
  if (!selectEl) return;
  selectEl.innerHTML = LANGS.map((l) => `<option value="${l.code}">${l.label}</option>`).join('');
  selectEl.value = current;
  selectEl.addEventListener('change', () => setLang(selectEl.value));
}

